/**
 * Initiates a deleteDoc operation for a document reference.
 * Does NOT await the write operation internally.
 */
export function deleteDocumentNonBlocking(docRef: DocumentReference) {
  // This function is designed for optimistic UI updates.
  // It triggers the delete and immediately returns.
  // The actual deletion happens in the background.
  // The UI should rely on the onSnapshot listener to remove the item upon successful deletion.
  deleteDoc(docRef)
    .catch(error => {
      // If the deletion fails due to permissions or other server errors,
      // this will catch it and emit a structured error.
      // The UI will eventually be corrected by the onSnapshot listener,
      // which will re-add the item if it wasn't successfully deleted.
      errorEmitter.emit(
        'permission-error',
        new FirestorePermissionError({
          path: docRef.path,
          operation: 'delete',
        })
      )
    });
}
